create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
    slot_count integer;
    normalized_username text;
begin
    perform pg_advisory_xact_lock(20260315);

    normalized_username := lower(
        trim(
            coalesce(
                new.raw_user_meta_data ->> 'username',
                split_part(coalesce(new.email, ''), '@', 1)
            )
        )
    );

    if normalized_username = '' then
        raise exception '아이디가 비어 있습니다.';
    end if;

    if normalized_username !~ '^[a-z0-9_]{4,20}$' then
        raise exception '아이디는 영문 소문자, 숫자, 밑줄만 사용해서 4~20자로 입력해주세요.';
    end if;

    select count(*) into slot_count
    from public.profiles;

    if slot_count >= 50 then
        raise exception '회원가입 가능 인원이 가득 찼습니다.';
    end if;

    insert into public.profiles (id, email, username)
    values (
        new.id,
        coalesce(new.email, ''),
        normalized_username
    )
    on conflict (id) do update
    set email = excluded.email,
        username = excluded.username;

    insert into public.categories (user_id, type, name, sort_order, is_active)
    values
        (new.id, 'expense', '식비', 0, true),
        (new.id, 'expense', '교통', 1, true),
        (new.id, 'expense', '주거', 2, true),
        (new.id, 'expense', '통신', 3, true),
        (new.id, 'expense', '의료', 4, true),
        (new.id, 'expense', '할부', 5, true),
        (new.id, 'expense', '기타', 6, true),
        (new.id, 'income', '월급', 0, true),
        (new.id, 'income', '상여', 1, true),
        (new.id, 'income', '부수입', 2, true),
        (new.id, 'investment', '주식', 0, true),
        (new.id, 'investment', '코인', 1, true),
        (new.id, 'investment', '예적금', 2, true),
        (new.id, 'fixed', '고정지출', 0, true)
    on conflict (user_id, type, name) do nothing;

    return new;
end;
$$;

