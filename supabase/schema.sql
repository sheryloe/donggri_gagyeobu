create or replace function public.validate_asset_row()
returns trigger
language plpgsql
set search_path = public
as $$
declare
    settlement_asset public.assets;
begin
    if new.type <> 'card' then
        new.card_settlement_day := null;
        new.card_settlement_asset_id := null;
        return new;
    end if;

    if new.card_settlement_day is null or new.card_settlement_day < 1 or new.card_settlement_day > 31 then
        raise exception '카드 자산은 결제일(1~31)이 필요합니다.';
    end if;

    if new.card_settlement_asset_id is null then
        raise exception '카드 자산은 결제 통장 설정이 필요합니다.';
    end if;

    select *
    into settlement_asset
    from public.assets
    where id = new.card_settlement_asset_id
      and user_id = new.user_id;

    if settlement_asset.id is null then
        raise exception '카드 결제 통장 자산을 찾을 수 없습니다.';
    end if;

    if settlement_asset.type not in ('cash', 'bank') then
        raise exception '카드 결제 통장은 은행/현금 자산이어야 합니다.';
    end if;

    if new.id is not null and settlement_asset.id = new.id then
        raise exception '카드 결제 통장은 자기 자신으로 설정할 수 없습니다.';
    end if;

    return new;
end;
$$;

drop trigger if exists validate_asset_row on public.assets;
create trigger validate_asset_row
before insert or update on public.assets
for each row execute function public.validate_asset_row();

create or replace function public.validate_transaction_row()
returns trigger
language plpgsql
set search_path = public
as $$
declare
    settlement_asset public.assets;
    card_asset public.assets;
begin
    select *
    into settlement_asset
    from public.assets
    where id = new.asset_id
      and user_id = new.user_id;

    if settlement_asset.id is null then
        raise exception '결제/정산 자산을 찾을 수 없습니다.';
    end if;

    if new.payment_method = 'card' then
        if new.type = 'income' then
            raise exception '수입은 카드 결제 방식으로 저장할 수 없습니다.';
        end if;

        if new.card_asset_id is null then
            raise exception '카드 자산을 선택해주세요.';
        end if;

        select *
        into card_asset
        from public.assets
        where id = new.card_asset_id
          and user_id = new.user_id;

        if card_asset.id is null then
            raise exception '카드 자산을 찾을 수 없습니다.';
        end if;

        if card_asset.type <> 'card' then
            raise exception '카드 결제에는 카드 자산이 필요합니다.';
        end if;

        if settlement_asset.type not in ('cash', 'bank') then
            raise exception '카드 정산 자산은 은행/현금 자산이어야 합니다.';
        end if;

        if new.settlement_date is null then
            raise exception '카드 결제는 결제일이 필요합니다.';
        end if;
    else
        new.card_asset_id := null;
        new.settlement_date := null;
    end if;

    return new;
end;
$$;

