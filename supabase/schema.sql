create or replace function public.delete_asset_cascade(p_asset_id bigint, p_force boolean default false)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
    uid uuid := auth.uid();
    target_asset public.assets;
    tx_count integer;
    fixed_count integer;
    investment_count integer;
    card_cfg_count integer;
    latest_tx record;
    tx_hint text := '';
begin
    if uid is null then
        raise exception '로그인이 필요합니다.';
    end if;

    select *
    into target_asset
    from public.assets
    where id = p_asset_id
      and user_id = uid;

    if target_asset.id is null then
        raise exception 'Asset not found';
    end if;

    select count(*) into tx_count
    from public.transactions
    where user_id = uid
      and (asset_id = p_asset_id or card_asset_id = p_asset_id);

    select count(*) into fixed_count
    from public.fixed_expenses
    where user_id = uid
      and asset_id = p_asset_id;

    select count(*) into investment_count
    from public.investments
    where user_id = uid
      and asset_id = p_asset_id;

    select count(*) into card_cfg_count
    from public.assets
    where user_id = uid
      and card_settlement_asset_id = p_asset_id;

    if (tx_count + fixed_count + investment_count + card_cfg_count) > 0 and not p_force then
        select date, type, description, amount
        into latest_tx
        from public.transactions
        where user_id = uid
          and (asset_id = p_asset_id or card_asset_id = p_asset_id)
        order by date desc, id desc
        limit 1;

        if latest_tx.date is not null then
            tx_hint := format(
                ' | 예시 거래: %s %s %s %s원',
                latest_tx.date,
                case latest_tx.type when 'income' then '수입' when 'investment' then '투자' else '지출' end,
                coalesce(latest_tx.description, '-'),
                to_char(latest_tx.amount, 'FM999,999,999,990')
            );
        end if;

        raise exception '연결된 데이터가 있어 삭제할 수 없습니다. 거래 %건, 고정지출 %건, 투자 %건, 카드결제설정 %건%s',
            tx_count, fixed_count, investment_count, card_cfg_count, tx_hint;
    end if;

    if p_force then
        delete from public.transactions
        where user_id = uid
          and (asset_id = p_asset_id or card_asset_id = p_asset_id);

        delete from public.fixed_expenses
        where user_id = uid
          and asset_id = p_asset_id;

        delete from public.investments
        where user_id = uid
          and asset_id = p_asset_id;

        update public.assets
        set card_settlement_asset_id = null
        where user_id = uid
          and card_settlement_asset_id = p_asset_id;
    end if;

    delete from public.assets
    where id = p_asset_id
      and user_id = uid;

    return jsonb_build_object('ok', true);
end;
$$;

grant execute on function public.delete_asset_cascade(bigint, boolean) to authenticated;

create or replace function public.clear_user_data()
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
    uid uuid := auth.uid();
